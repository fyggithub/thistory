#ifndef _DATATYPES_H_
#define _DATATYPES_H_
/********************************************************************
 * Re-define data types
 *******************************************************************/
#ifndef UINT8
	#define UINT8 unsigned char
#endif

#ifndef UINT16
	#define UINT16 unsigned short int
#endif

#ifndef UINT32
	#define UINT32 unsigned int
#endif

#ifndef INT8
	#define INT8 signed char
#endif

#ifndef INT16
	#define INT16 short int
#endif

#ifndef INT32
	#define INT32 int
#endif

#ifndef BOOL
	#define BOOL int
#endif

#ifndef FALSE
	#define FALSE (0)
#endif

#ifndef TRUE
	#define TRUE (1)
#endif

#ifndef NULL
	#define NULL (0)
#endif

#ifndef VOID
	#define VOID void
#endif

#ifndef BYTE
	#define BYTE UINT8
#endif

#ifndef WORD
	#define WORD UINT16
#endif

#ifndef DWORD
	#define DWORD UINT32
#endif

#ifndef FLOAT
	#define FLOAT float
#endif

#ifndef DOUBLE
	#define DOUBLE double
#endif

#ifndef CHAR
	#define CHAR char
#endif

#ifndef STATIC
	#define STATIC static
#endif

#ifndef CONST
	#define CONST const
#endif

#ifndef VOLATILE
	#define VOLATILE volatile
#endif
/********************************************************************
 * End of File
 *******************************************************************/
#endif // _DATATYPES_H_
